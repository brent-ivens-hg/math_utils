    import requests
    from bs4 import BeautifulSoup
    from re import compile

    from utils.formatting import F5, rounds

    NON_FLOAT_SYMBOLS = compile(r'[^.\d]+')
    OEIS_CODE = compile(r'A\d{6}')


    def fetch_constant(url: str) -> None:
        """
        Fetches Constant From OEIS url

        :returns: formatted constant (string)
        """
        code = OEIS_CODE.search(url)[0]
        req = requests.get(f'https://oeis.org/{code}/constant')
        soup = BeautifulSoup(req.text, 'html.parser')
        digits = soup.find('pre').text

        number = NON_FLOAT_SYMBOLS.sub('', digits)
        integer, fractional = number.split('.')
        fractional = rounds(fractional, ndigits=35)

        print(F5.format(f'{integer}.{fractional}'))


